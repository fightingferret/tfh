//////////////////////////////////////////////////////////////////////////
// Function Name:  get_eq_char()
//////////////////////////////////////////////////////////////////////////
obj_data*
get_eq_char( char_data* ch, int slot )
{
   return( ch->Wearing(slot) );
}

//////////////////////////////////////////////////////////////////////////
// Function Name:  get_char_world()
//////////////////////////////////////////////////////////////////////////
char_data*
get_char_world( char_data* ch, char* argument, char* verb )
{
   return ( one_character( ch, argument, verb, ch->array,
                           (thing_array*)&player_list, 
			   (thing_array*)&mob_list ) );
}

//////////////////////////////////////////////////////////////////////////
// Function Name:  get_char_room()
//////////////////////////////////////////////////////////////////////////
char_data*
get_char_room( char_data* ch, char* argument, char* verb)
{
   return ( one_character( ch, argument, verb, ch->array ) );
}
